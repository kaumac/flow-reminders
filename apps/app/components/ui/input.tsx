import * as React from "react"

import { cn } from "@/lib/utils"

function Input({ className, type, ...props }: React.ComponentProps<"input">) {
  return (
    <input
      type={type}
      data-slot="input"
      className={cn(
        "dark:bg-input/30 border-input focus-visible:border-ring focus-visible:ring-ring/50 aria-invalid:ring-destructive/20 dark:aria-invalid:ring-destructive/40 aria-invalid:border-destructive dark:aria-invalid:border-destructive/50 disabled:bg-input/50 dark:disabled:bg-input/80 h-10 rounded-lg border bg-transparent px-3 py-2 text-base transition-colors file:h-6 file:text-sm file:font-medium focus-visible:ring-[3px] aria-invalid:ring-[3px] md:text-sm file:text-foreground placeholder:text-muted-foreground w-full min-w-0 outline-none file:inline-flex file:border-0 file:bg-transparent disabled:pointer-events-none disabled:cursor-not-allowed disabled:opacity-50",
        className
      )}
      {...props}
    />
  )
}


function InputGroup({ className, children, ...props }: React.ComponentProps<"div">) {
  return (
    <div
      data-slot="input-group"
      className={cn("relative flex items-center w-full", className)}
      {...props}
    >
      {children}
    </div>
  )
}

function InputLeftAddon({ className, children, ...props }: React.ComponentProps<"div">) {
  return (
    <div
      data-slot="input-left-addon"
      className={cn("absolute left-2.5 top-1/2 -translate-y-1/2 text-muted-foreground pointer-events-none", className)}
      {...props}
    >
      {children}
    </div>
  )
}

function InputRightAddon({ className, children, ...props }: React.ComponentProps<"div">) {
  return (
    <div
      data-slot="input-right-addon"
      className={cn("absolute right-2.5 top-1/2 -translate-y-1/2 text-muted-foreground", className)}
      {...props}
    >
      {children}
    </div>
  )
}

export { Input, InputGroup, InputLeftAddon, InputRightAddon }
